//The following numerical operations also apply for float
//Incase the result is a perfect integer the decimals(.000) will be removed

//Addition
var a = 5;
a = a + 1; //6

//Subtraction
var b = 10;
b -= 5; //5

//concatenation of two strings
var c = "I am a ";
c = c + "String"; //I am a String

//Multiplication
var d = 20;
d *= 2; //40

//Divison
var e = 50;
e /= 2; //25

//Remainder
var f = 4;
f %= 3; //1

//Increment
var g = 55;
g++; //g=56

//Decrement
var h = 55;
h--; //h=54