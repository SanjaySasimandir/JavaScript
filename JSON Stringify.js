// JSON.stringify makes the value of the variable into a string

var array = [1, 2, 3, 4, 5, 6];
console.log("Array is: " + JSON.stringify(array)); // Array is: [1,2,3,4,5,6]

var num = 6;
console.log("Number is: " + JSON.stringify(num)); // Number is: 6

var flag = true;
console.log("Boolean Value is: " + JSON.stringify(flag)); // Boolean Value is: true